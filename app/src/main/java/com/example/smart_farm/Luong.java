package com.example.smart_farm;

public class Luong {
    public String Name;
    public String Doam;

    public Luong(String name, String doam) {
        Name = name;
        Doam = doam;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getDoam() {
        return Doam;
    }

    public void setDoam(String doam) {
        Doam = doam;
    }
}
